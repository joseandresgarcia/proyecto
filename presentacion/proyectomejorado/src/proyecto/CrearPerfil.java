     public void Eliminar(int CodigoHorario){
        
        conectar cc= new conectar();
        Connection cn = cc.conexion(); 
        
        Statement instruccion;
        try {
            instruccion = cn.createStatement();
            int  res= instruccion.executeUpdate("delete From Perfil where Codigo="+ CodigoHorario);
            
            if (res>0){
                JOptionPane.showMessageDialog(null, "Se elimino el Perfil ");
            }else
                JOptionPane.showMessageDialog(null, "No se ha podido eliminar el Perfil" );
    
        } catch (SQLException ex) {
            Logger.getLogger(CrearHorario_new.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }
    
    public void Listar(){
        
        conectar cc= new conectar();
        Connection cn = cc.conexion(); 
        
        Statement instruccion;
        
        try {
            instruccion = cn.createStatement();
            ResultSet  tabla= instruccion.executeQuery(
                    "select Codigo,Descripcion,Activo Estado from perfil;");
            
            String Cabecera[]={"Codigo","DescripciÃ³n","Estado"};
            String Datos[][]={};
            DefaultTableModel tab=new DefaultTableModel(Datos,Cabecera);
            tblPerfiles.setModel(tab);
            
            while(tabla.next())
            {
                Object LisaHorario[]={Integer.toString(tabla.getInt("Codigo")),tabla.getString("Descripcion"),Boolean.toString(tabla.getBoolean("Estado"))};
                tab.addRow(LisaHorario);
            }    
    
        } catch (SQLException ex) {
            Logger.getLogger(CrearHorario_new.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    
    
