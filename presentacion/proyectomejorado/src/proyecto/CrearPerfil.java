    private void tblPerfilesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPerfilesMouseClicked
        // TODO add your handling code here:
        if (evt.getClickCount()==2){
            int fila=tblPerfiles.getSelectedRow();
            txtCodigo.setText(tblPerfiles.getValueAt(fila, 0).toString());
            txtDescripcion.setText(tblPerfiles.getValueAt(fila, 1).toString());
            if (Boolean.parseBoolean(tblPerfiles.getValueAt(fila, 2).toString()))
            {
              chbEstado.setSelected(true);
            }
            else chbEstado.setSelected(false);
        }
    }//GEN-LAST:event_tblPerfilesMouseClicked

