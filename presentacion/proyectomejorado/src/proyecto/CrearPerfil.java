    private void btnAlmacenarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlmacenarActionPerformed
        String Codigo, Perfil;
        boolean Estado=false;
        Codigo=txtCodigo.getText();
        Perfil=txtDescripcion.getText();
        if(chbEstado.isSelected()){Estado=true;}
        Almacenar(Codigo, Perfil,Estado);
        Listar();
    }//GEN-LAST:event_btnAlmacenarActionPerformed

    private void Almacenar(String Codigo, String Perfil,boolean Estado){
              
        conectar cc= new conectar();
        Connection cn = cc.conexion(); 
        
        Statement instruccion;
        try {
            instruccion = cn.createStatement();
            int  res;
            if (Codigo.isEmpty()){
                res= instruccion.executeUpdate("insert into Perfil (Descripcion, Activo) "
                    + " values ('"+Perfil+"',"+Boolean.toString(Estado)+");");
            }else {
                res= instruccion.executeUpdate("update Perfil  set Descripcion='"+Perfil+"', Activo= "+Boolean.toString(Estado)
                    + " where Codigo="+Codigo+";");
            }
            
           
            if (res>0){
                JOptionPane.showMessageDialog(null, "Se Actualizo el perfil");
                Limpiar();
            }else
                JOptionPane.showMessageDialog(null, "No se ha actualizado el perfil" );
        } catch (SQLException ex) {
            Logger.getLogger(CrearHorario_new.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
